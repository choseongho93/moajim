/**
 * 시/도 목록
 */
export function getCities(): string[] {
  return Object.keys(LAWD_CODES)
}

/**
 * 시/군/구 목록
 */
export function getDistricts(city: string): string[] {
  return Object.keys(LAWD_CODES[city as keyof typeof LAWD_CODES] || {})
}

/**
 * 법정동 코드 조회
 */
export function getLawdCode(city: string, district: string): string | null {
  const cityData = LAWD_CODES[city as keyof typeof LAWD_CODES]
  if (!cityData) return null
  return cityData[district as keyof typeof cityData] || null
}
