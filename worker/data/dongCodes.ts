/**
 * 시/군/구 코드로 동/리 목록 조회 (한글 오름차순 정렬)
 */
export function getDongList(lawdCd: string): string[] {
  return Object.keys(DONG_CODES[lawdCd] || {}).sort((a, b) => a.localeCompare(b, 'ko-KR'))
}

/**
 * 시/군/구 코드와 동/리명으로 법정동 코드 조회
 */
export function getDongCode(lawdCd: string, dongName: string): string | null {
  const dongs = DONG_CODES[lawdCd]
  if (!dongs) return null
  return dongs[dongName] || null
}

/**
 * 해당 지역에 동 코드 데이터가 있는지 확인
 */
export function hasDongData(lawdCd: string): boolean {
  return !!DONG_CODES[lawdCd]
}
